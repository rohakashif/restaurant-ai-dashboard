export function getFoodById(id: string): Food | undefined {
  return foods.find((f) => f.id === id);
}

export function getFoodsByCategory(category: string): Food[] {
  if (category === "All") return foods;
  return foods.filter((f) => f.category === category);
}

export function getRelatedFoods(foodId: string, limit = 4): Food[] {
  const food = getFoodById(foodId);
  if (!food) return foods.slice(0, limit);
  return foods
    .filter((f) => f.id !== foodId && f.category === food.category)
    .slice(0, limit);
}
